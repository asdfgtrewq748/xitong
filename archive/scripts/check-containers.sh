#!/bin/bash
# ============================================================================
# 检查 Docker 容器状态脚本
# ============================================================================

echo "========================================"
echo "检查 Docker 容器状态"
echo "========================================"

echo -e "\n[1] 检查所有容器:"
echo "----------------------------------------"
docker ps -a

echo -e "\n[2] 检查正在运行的容器:"
echo "----------------------------------------"
docker ps

echo -e "\n[3] 检查 Docker Compose 服务:"
echo "----------------------------------------"
docker compose ps

echo -e "\n[4] 检查网络:"
echo "----------------------------------------"
docker network ls | grep mining

echo -e "\n[5] 检查镜像:"
echo "----------------------------------------"
docker images | grep mining

echo -e "\n========================================"
echo "提示:"
echo "  - 如果容器没有运行,执行: docker compose up -d"
echo "  - 查看服务日志: docker compose logs [服务名]"
echo "  - 重启服务: docker compose restart"
echo "========================================"
